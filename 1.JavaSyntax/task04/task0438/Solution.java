package com.javarush.task.task04.task0438;

/* 
Рисуем линии
*/

public class Solution {
    public static void main(String[] args) {
        int eight = 8;
        for (int i = 0; i < 9; i++) {
            System.out.print(eight);
        }
        for (int j = 0; j < 11; j++) {
            System.out.println(eight);
        }

    }
}
