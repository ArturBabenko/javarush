/* 
Гадание на долларовый счет
*/

public class Solution {
    public static void main(String[] args) {
        String S;
        int j = 0;
        while (j < 10) {
            int i = 0;
            while (i < 10) {
                System.out.print("S");
                i++;
            }
            j++;
            System.out.println("");
        }

    }
}
